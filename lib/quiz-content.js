function makePack(options) {
  return options.map((option, index) => ({
    optionKey: OPTION_KEYS[index],
    quadrant: QUADRANT_KEYS[index],
    text: option.text,
    tags: option.tags || {},
  }));
}

function quadrantScores(theme, quadrant, tags) {
  const [axis1, axis2] = theme.axes;
  const scores = {
    [quadrant[0] === "l" ? axis1.leftKey : axis1.rightKey]: 2,
    [quadrant[1] === "l" ? axis2.leftKey : axis2.rightKey]: 2,
  };

  return {
    quadrant,
    scores,
    tags,
  };
}

