function buildQuestions(theme) {
  return theme.contexts.flatMap((context, contextIndex) =>
    theme.promptTemplates.map((template, templateIndex) => {
      const code = `${theme.code}-${String(contextIndex * theme.promptTemplates.length + templateIndex + 1).padStart(2, "0")}`;
      const prompt = template.replace("{context}", context);
      const pack = theme.packs[templateIndex];

      return {
        id: `${theme.slug}-${code.toLowerCase()}`,
        code,
        prompt,
        contextHint: context,
        sortOrder: contextIndex * theme.promptTemplates.length + templateIndex + 1,
        options: pack.map((option) => ({
          id: `${theme.slug}-${code.toLowerCase()}-${option.optionKey.toLowerCase()}`,
          optionKey: option.optionKey,
          text: option.text,
          traitWeights: quadrantScores(theme, option.quadrant, option.tags),
        })),
      };
    }),
  );
}

function buildTopic(theme) {
  const questions = buildQuestions(theme);
  const resultProfiles = buildProfiles(theme);

  return {
    slug: theme.slug,
    code: theme.code,
    icon: theme.icon,
    title: theme.title,
    shortTitle: theme.shortTitle,
    subtitle: theme.subtitle,
    description: theme.description,
    cardDescription: theme.cardDescription,
    heroCopy: theme.heroCopy,
    shareTitle: theme.shareTitle,
    shareDescription: theme.shareDescription,
    locale: "zh-TW",
    themeColor: theme.themeColor,
    themeColorSoft: theme.themeColorSoft,
    themeInk: theme.themeInk,
    themeGlow: theme.themeGlow,
    domain: theme.domain,
    axes: theme.axes,
    tagLabels: theme.tagLabels,
    questionPoolSize: questions.length,
    questionsPerSession: QUESTIONS_PER_SESSION,
    questions,
    resultProfiles,
  };
}

